#include <bits/stdc++.h>
using namespace std;
int main()
{
    //Time complexity of map is O(log(n)) and unordered_map is O(1) on average
    unordered_map<string, int> mpp; // Does not sort the data in ascending order lexicographically
    mpp["tv"] = 50;
    mpp["laptop"] = 100;
    mpp["headphones"] = 50;
    
    return 0;
}